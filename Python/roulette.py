x = int(input())

if x == 0:
    print('зеленый')
elif (1 <= x <= 10) or (19 <= x <= 28):
    if x % 2 == 0:
        print('черный')
    else:
        print('красный')
elif (11 <= x <= 18) or (29 <= x <= 36):
    if x % 2 == 0:
        print('красный')
    else:
        print('черный')
else:
    print('ошибка ввода')

#0 - зеленый,
# от 1 до 10 и от 19 до 28: чет(черный)/нечет(красный),

# от 11 до 18 и от 29 до 36: чет(красный)/нечет(черный)
